package com.sample.maps;

import java.util.HashMap;

public class MapExample {
	
	
	public static void main(String[] args) {
		
		HashMap<String,String> map = new HashMap<>();
		map.put("name","deep");
		System.out.println(map.get("name"));
	}

	
	/**
	 * Hashmap
	 * 
	 * To set key vvalue based
	 * u can put key  and value for specific key
	 * 
	 * map.put key,value
	 * map.get - key
	 * 
	 * clear()
	 * compute
	 * entryset
	 * containskey
	 * 
	 * 
	 * 
	 * Difference
	 * 
	 * HashMap -- is Class
	 * Map -- Interface
	 * 
	 * hashmap -- subset of map
	 * Map -- is a super set
	 * 
	 * hashmap -- has all predefined functions of map
	 * Map -- we canot crate a instance-- Map m = new hashmap();
	 * 
	 * 
	 */
}
